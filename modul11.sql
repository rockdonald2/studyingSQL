Database,
  adatok kollekcióját amelyet úgy állítottak össze, hogy könnyen elérhetők legyenek, és hatékonyan lehessen kezelni valamit frissíteni
    adatbázisoknak nevezzük.

    - az adatbázisok táblazatokból épülnek fel melyek lényeges információkat tartalmaznak.


Database tables,
  a táblázatok raktároznak és megjelenítenek adatokat rendezett formában oszlopokban és sorokban, hasonlít az Excel táblázatkezelőjére.

    - az adatbázisok általában többet tartalmaznak ezekből, mindegyiknek megvan a saját meghatározott célja

    - mindegyik táblázatnak megvan a saját mezőkészlete, arra az adatra alapozva amit tartalmazni fog.

    ! egy táblázatnak meghatározott számú oszlopa van, azonban végtelen sora.


Primary keys,
  az elsődleges kulcs az egy mező a táblázatból amely egyedien beazonosítja a táblázat tartalmát.

    ! tulajdonságai:
        - minden sornak egyedi kulcsa kell legyenek
        - nem tartalmazhat NULL értéket

What is SQL?
  az SQL a Structured Query Language rövidítése, azaz magyarul a rendezett lekérdezési nyelv.

    - az SQL-t adatbázisok elérése és befolyásolására használják
    - a MySQL az egy program ami megérti az SQL-t.

    az SQL képes:
      - beilleszteni, frissíteni, vagy törölve adatokat az adatbázisból
      - új adatbázisokat, táblázatokat, mentett eljárásokat, nézeteket létrehozni
      - lekérni adatot az adatbázisból, stb.
